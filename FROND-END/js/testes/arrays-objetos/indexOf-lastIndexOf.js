// O método indexOf() encontra o índice de um determinado elemento
// O métido lastIndexOf() encontra o último índice de um elemento

let frutas = [
  "uva",
  "abacaxi",
  "melão",
  "banana",
  "abacate",
  "melancia",
  "caju",
  "manga",
];

console.log(frutas.indexOf("melancia"));
console.log(frutas.lastIndexOf("manga"));
